import math as m

print(m.sqrt(16))  # 4.0
print(m.pi)  # 3.141592653589793
print(m.pow(2, 3))  # 8.0
print(m.e)  # 2.718281828459045
print(m.floor(3.999))  # 3
print(m.ceil(3.999))  # 4
r = 5
print("Area of circle: ", m.pi * m.pow(r, 2))  # Area of circle:  78.53981633974483
print(m.inf)  # inf
print(m.nan)  # nan
